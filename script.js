function add(num1, num2) {
    return num1 + num2;
}

function subtract(num1, num2) {
    return num1 - num2;
}

function multiply(num1, num2) {
    return num1 * num2;
}

function divide(num1, num2) {
    return num1 / num2;
}

function plusminus(num1) {
    return num1 * -1 ;
}

function percent(num1) {
    return num1 / 100; 
}